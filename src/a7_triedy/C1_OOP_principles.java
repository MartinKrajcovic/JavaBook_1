package a7_triedy;
/*
 * Java je vyhradne objektovo orientovany jazyk.
 * Objektovo orientovane programovanie je zalozene na objektoch
 * a im prislusnych triedach, z ktorych sa programy skladaju.
 *
 * OOP (objektovo orientovane programovanie) vychadza z myslienky,
 * ze vsetky programy, ktore vytvarame, su simulaciou bud skutocneho
 * alebo vymysleneho virtualneho sveta.
 * Cim bude simulacia presnejsia, tym bude vysledny program lepsi.
 * 
 * V objektovo orientovanych programoch je za objekt povazovane
 * vsetko co mozeme nazvat podstatnym menom.
 * 
 * Tri zakladne principy OOP, ktore by sa mali dodrziavat su 
 * zapuzdrenie, dedicnost a polymorfizmus (mnohotvarnost).
 * 
 * 	1. ZAPUZDRENIE: 
 * 					Je to princip zakryvania atributov z hladiska
 * 					bezpecnosti. Kazdy objekt ma svoje specifcke atributy
 * 					ktore su dane iba jemu a nie je mozne ich nikym inym
 * 					zmenit. Moze sa k nim pristupovat len cez pristupove
 * 					funkcie objektu, ktory ich vlastni.
 * 					Napriklad taky televizor, ten ma zapuzdrene vnutro tak,
 * 					ze je obalene kopov plastovych krytov a srobov, aby iny
 * 					nevideli co je ako pozapajane.
 * 					
 *  2. DEDICNOST:
 *  				Je schopnost objektov dedit vlastnosti od ich
 *  				rodicovskych objektov a ich predkov.
 *  				Ako priklad mozeme uviest aj taky mobil.
 *  				Mobilne telefony kedysi vznikli za ucelom moznosti
 *  				komunikacie v pohybe. Dnes mame mobilne telefony,
 *  				ktore su obohatene o stovky dalsich funkcii, no
 *  				moznost telefonovania zdedili od svojich predkov.
 *  
 *  3. POLYMORFIZMUS:
 *  				Je to schopnost objektov reagovat na rovnaku
 *  				spravu rozne.
 *  				Predstav si nejaky hokejovy zapas. Na zapase
 *  				su fanusikovia dvoch typov: pre tym A a pre tym B.
 *  				Ked padne gol, je zrejme, ze fanusik fandiaci tymu
 *  				A bude reagovat na gol inak ako fanusik tymu B.
 */