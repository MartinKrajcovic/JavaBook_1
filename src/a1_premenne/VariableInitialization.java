package a1_premenne;
/**
 * Tato trieda demonstruje inicializaciu premennych a konstant;
 */
public class VariableInitialization {
	/*
	 * Priklad premennej, ktora neuchovava ziadnu hodnotu je samotna
	 * deklaracia premennej.
	 * 
	 * Pokial chceme mat premennu, ktora bude reprezentovat nejaku
	 * konkretnu hodnotu napriklad cislo, znak alebo logicky stav,
	 * musime ju inicializovat.
	 * Inymi slovami to znamena priradit jej hodnotu.
	 * To sa robi pomocou operatora priradenia '='.
	 */
	
	double average = 45.22;		//inicializacia premennej 'average' typu 'double' na hodnotu '45,22'
	final char DOT = '.';		//priradenie hodnoty '.' (bodka) premennej 'dot' typu 'char'
	
}
